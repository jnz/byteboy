#define BB_MAX_CODE_SIZE       0xFFFF /**< max. number of bytes for ASM
                                           instructions */

/******************************************************************************
 * TYPEDEFS
 ******************************************************************************/

typedef struct bb_reg_s
{
    union {
        struct
        {
            /* f: flags register. lower 4 bits are always zero.
             *    bit 7: zero.
             *    bit 6: subtraction.
             *    bit 5: half carry
             *    bit 4: carry */
            uint8_t a, f, b, c, d, e, h, l;
        };
        struct
        {
            uint16_t af, bc, de, hl;
        };
        uint8_t arr[8];
    };
} bb_reg_t;

typedef struct bb_state_s
{
    bb_reg_t reg;
    uint8_t code[BB_MAX_CODE_SIZE];


} bb_state_t;

